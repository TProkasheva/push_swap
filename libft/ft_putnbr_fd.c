#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	long int n1;

	n1 = n;
	if (n1 < 0)
	{
		ft_putchar_fd('-', fd);
		n1 *= -1;
	}
	if (n1 > 9)
		ft_putnbr_fd(n1 / 10, fd);
	ft_putchar_fd('0' + (n1 % 10), fd);
}
