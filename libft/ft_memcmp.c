#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t length)
{
	char	*src1;
	char	*src2;
	size_t	i;

	src1 = (char*)s1;
	src2 = (char*)s2;
	if (!src1 && !src2)
		return (0);
	i = 0;
	while (i < length)
	{
		if (src1[i] != src2[i])
			return ((unsigned char)src1[i] - (unsigned char)src2[i]);
		i++;
	}
	return (0);
}
